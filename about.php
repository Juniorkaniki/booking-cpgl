<h2 class="mb-4">Quel est le rôle de la DGM en RDC ?</h2>
<p>La DGM est un service public de l'Etat congolais chargé de l'exécution de la politique du gouvernement en terme d'immigration.</p>
<p>La Direction Générale de Migration (DGM), également connue sous le nom de Direction Générale des Migrations , est une agence relevant du ministère de l'Intérieur et de la Sécurité de la République démocratique du Congo . Il est chargé de gérer les affaires liées à la migration et à l'immigration , y compris des questions telles que les visas , les permis et le contrôle des frontières , afin de réglementer et de gérer les mouvements de personnes entrant et sortant du pays. Le siège du DGM est situé à Gombe, Kinshasa .</p>
